def get_headers():
    return {
        "apikey": SUPABASE_SERVICE_KEY,
        "Authorization": f"Bearer {SUPABASE_SERVICE_KEY}"
    }

def main():
    vins = [v.strip() for v in vins_raw.split("\n") if v.strip()]
    print(f"📊 ĐANG THỐNG KÊ CHO DANH SÁCH {len(vins)} SỐ VIN...")
    
    stats = {}
    found_count = 0
    
    # Chia nhỏ list VIN để query tránh URL quá dài
    batch_size = 20
    for i in range(0, len(vins), batch_size):
        batch = vins[i:i+batch_size]
        vin_str = ",".join(batch)
        url = f"{SUPABASE_URL}/rest/v1/yeucauxhd?vin=in.({vin_str})&select=tvbh,vin"
        
        resp = requests.get(url, headers=get_headers())
        if resp.status_code == 200:
            data = resp.json()
            for order in data:
                tvbh = order.get('tvbh', 'Chưa rõ TVBH').strip()
                stats[tvbh] = stats.get(tvbh, 0) + 1
                found_count += 1
        else:
            print(f"Lỗi truy vấn batch {i}: {resp.text}")

    # Sắp xếp
    sorted_stats = sorted(stats.items(), key=lambda x: x[1], reverse=True)

    print("\n" + "=" * 60)
    print("🏆 BẢNG VÀNG THÀNH TÍCH (DỰA TRÊN 102 VIN CỦA BẠN)")
    print("=" * 60)
    print("{:<30} | {:<10}".format("Tư vấn bán hàng", "Số lượng"))
    print("-" * 45)
    for name, count in sorted_stats:
        print("{:<30} | {:<10} xe".format(name.upper(), count))
    
    print("\n" + "=" * 60)
    best_sale = sorted_stats[0][0]
    max_cars = sorted_stats[0][1]
    print(f"🏆 NGƯỜI CHIẾN THẮNG CUỐI CÙNG: \n⭐️ {best_sale.upper()} (với {max_cars} xe)")
    print(f"\n(Tìm thấy thông tin cho {found_count}/{len(vins)} VIN trong hệ thống)")
    print("=" * 60)

if __name__ == "__main__":
    main()
